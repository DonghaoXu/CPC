classdef air < Property
    properties (SetAccess=private)
        name='air';
    end
    methods 
        function y=cp(~,T)
            % Temperature(K)  Specific heat(kJ/kg-K)
            % 200             1.0025
            % 225             1.0027
            % 250             1.0031
            % 275             1.0038
            % 300             1.0049
            % 325             1.0063
            % 350             1.0082
            % 375             1.0106
            % 400             1.0135
            % 450             1.0206
            % 500             1.0295
            y=3.451e-4*T.^2-1.529e-1*T+1020;    %J/kg-K
        end
        
        function y=rho(~,T)
            % Temperature(K)	Density(kg/m3)
            % 248             1.4224
            % 253             1.3943
            % 258             1.3673
            % 263             1.3413
            % 268             1.3163
            % 273             1.2922
            % 278             1.2690
            % 283             1.2466
            % 288             1.2250
            % 293             1.2041
            % 298             1.1839
            % 303             1.1644
            % 308             1.1455
            % 313             1.1270
            % 323             1.1090
            % 333             1.0600
            % 343             1.0290
            % 353             0.9996
            % 363             0.9721
            % 373             0.9461
            % 473             0.7461
            y=361.6./(T-7.13);  %kg/m3
        end
        
        function y=mu(~,T)
            % Temperature(K)  Dynamic viscosity(1e-5 Pa-s or kg/m-s)
            % 200             1.329
            % 225             1.467
            % 250             1.599
            % 275             1.725
            % 300             1.846
            % 325             1.962
            % 350             2.075
            % 375             2.181
            % 400             2.286
            % 450             2.485
            % 500             2.670
            y=0.1458*T.^1.5./(T+110.5)*1e-5;    %kg/m-s
        end
        
        function y=k(~,T)
            % Temperature(K)  Thermal Conductivity(1e-5 kW/m-K)
            % 200             1.809
            % 225             2.020
            % 250             2.227
            % 275             2.428
            % 300             2.624
            % 325             2.816
            % 350             3.003
            % 375             3.186
            % 400             3.365
            % 450             3.710
            % 500             4.041
            y=(0.345*sqrt(T+196.6)-5.063)*1e-2; %W/m-K
        end
    end
end